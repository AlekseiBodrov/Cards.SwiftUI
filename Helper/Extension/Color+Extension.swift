import SwiftUI

extension Color {
    public static var white: Color {
        return Color.init("#ffffff")
    }
    public static var black: Color {
        return Color.init("1a1a1a")
    }
    public static var red: Color {
        return Color.init("ff3044")
    }
    public static var blue: Color {
        return Color.init("2688eb")
    }
    public static var darkGrey: Color {
        return Color.init("949494")
    }
    public static var lightGrey: Color {
        return Color.init("efefef")
    }
}
